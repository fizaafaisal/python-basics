#The range() function

#To loop through a set of code a specefied number of times,
# we can use the range() function,
# The range() function returns a sequence of numbers,
#starting from 0 by default, and increment by 1 (by default ),
# and ends at a specified number.


for x in range(6):
    print(x)

# note-- that range(6) is not the value of 0 to 6,
# but the value 0 to 5
