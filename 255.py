#1-Calculate the square of a number and return the result
# using return statement
def square_num(x):
    return x * x
print(square_num(4))
print(square_num(7))
print(square_num(10))

# 2-Concatenate two strings and return the result using
# return statement

def concatenate_string(str1, str2):
    return str1 + str2
print(concatenate_string("hello ", "world!"))
print(concatenate_string("python ", "rocks!"))